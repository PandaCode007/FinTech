function bfCalcLoan() {
    var P = parseFloat(document.getElementById('loan-amount').value) || 0;
    var apr = parseFloat(document.getElementById('loan-rate').value) || 0;
    var n = parseFloat(document.getElementById('loan-term').value) || 1;
    var r = apr / 100 / 12;
    var monthly = r ? (P * r) / (1 - Math.pow(1 + r, -n)) : P / n;
    var total = monthly * n;
    var fmt = function (v) { return '$' + Math.round(v).toLocaleString(); };
    document.getElementById('loan-monthly').textContent = fmt(monthly);
    document.getElementById('loan-interest').textContent = fmt(total - P);
    document.getElementById('loan-total').textContent = fmt(total);
}
bfCalcLoan();
</script>

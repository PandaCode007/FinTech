function bfCalcSavings() {
    var P = parseFloat(document.getElementById('calc-initial').value) || 0;
    var m = parseFloat(document.getElementById('calc-monthly').value) || 0;
    var yrs = parseFloat(document.getElementById('calc-years').value) || 0;
    var r = 0.045 / 12;
    var n = yrs * 12;
    var future = P * Math.pow(1 + r, n) + (r ? m * ((Math.pow(1 + r, n) - 1) / r) : m * n);
    var contributed = P + m * n;
    var fmt = function (v) { return '$' + Math.round(v).toLocaleString(); };
    document.getElementById('calc-result').textContent = fmt(future);
    document.getElementById('calc-interest').textContent = fmt(future - contributed);
}
bfCalcSavings();
</script>

function bfCalcInvest() {
    var P = parseFloat(document.getElementById('inv-initial').value) || 0;
    var m = parseFloat(document.getElementById('inv-monthly').value) || 0;
    var yrs = parseFloat(document.getElementById('inv-years').value) || 0;
    var r = 0.07 / 12;
    var n = yrs * 12;
    var future = P * Math.pow(1 + r, n) + (r ? m * ((Math.pow(1 + r, n) - 1) / r) : m * n);
    var contributed = P + m * n;
    var fmt = function (v) { return '$' + Math.round(v).toLocaleString(); };
    document.getElementById('inv-result').textContent = fmt(future);
    document.getElementById('inv-gain').textContent = fmt(future - contributed);
}
bfCalcInvest();
</script>
